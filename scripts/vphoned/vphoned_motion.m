/*
 * vphoned_motion — shake gesture via Mach injection of a helper dylib.
 *
 * Flow on `{"t":"shake"}`:
 *   1. Enumerate running user apps (proc_listpids + path filter), drop the
 *      ones task_for_pid rejects (system apps with get-task-allow=false),
 *      pick the most-foreground one by BKSApplicationStateMonitor state.
 *   2. Resolve the target's data container via LSApplicationWorkspace and
 *      deploy the embedded helper dylib into <container>/tmp/. That's the
 *      only path guaranteed readable by a sandboxed app.
 *   3. task_for_pid + mach_vm_allocate code/path/stack/args in target.
 *   4. thread_create_running runs the OUTER shellcode on a raw Mach thread,
 *      which calls pthread_create_from_mach_thread to spawn an INNER pthread.
 *      The inner has proper TLS, so it can safely call dlopen(path, RTLD_NOW)
 *      — calling dlopen directly from a raw Mach thread crashes inside
 *      pthread_getspecific.
 *   5. After inject, send SIGUSR2 to fire the shake. Works for both fresh
 *      loads (constructor installed the handler) and cached dlopens (handler
 *      from a prior load is still resident).
 *   6. Subsequent shakes for the same pid skip inject and just SIGUSR2.
 *
 * Shellcode uses paciza / blraaz, which authenticate as PAC ops on arm64e
 * and act as NOP+BLR on arm64 — same bytes work for both target ABIs.
 */

